

class Calculator
    def self.add(value1, value2)
        return value1 + value2
    end

    def self.subtract(value1, value2)
        return value1 - value2
    end

    def self.multiply(value1, value2)
        return value1 * value2
    end

    def self.divide(value1, value2)
        return value1 / value2
    end
end


